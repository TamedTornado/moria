#[cfg(not(test))]
fn acceptance_machine_profile() -> Result<MachineProfile, CommandError> {
    if std::env::consts::OS != "macos" || std::env::consts::ARCH != "aarch64" {
        return Err(CommandError::Report(ReportValidationError::Identity {
            field: "M4 machine",
        }));
    }
    let os_version = command_output("sw_vers", ["-productVersion"])
        .map_err(|_| CommandError::Report(ReportValidationError::Identity { field: "machine" }))?;
    let cpu_model = command_output("sysctl", ["-n", "machdep.cpu.brand_string"])
        .map_err(|_| CommandError::Report(ReportValidationError::Identity { field: "machine" }))?;
    let logical_cores = command_output("sysctl", ["-n", "hw.logicalcpu"])
        .ok()
        .and_then(|value| value.parse().ok())
        .ok_or(CommandError::Report(ReportValidationError::Identity {
            field: "machine",
        }))?;
    let total_physical_memory_bytes = command_output("sysctl", ["-n", "hw.memsize"])
        .ok()
        .and_then(|value| value.parse().ok())
        .ok_or(CommandError::Report(ReportValidationError::Identity {
            field: "machine",
        }))?;
    Ok(MachineProfile {
        profile_id_sha256: hex_sha256(
            format!("macOS|{os_version}|{cpu_model}|{logical_cores}|{total_physical_memory_bytes}")
                .as_bytes(),
        ),
        os_name: "macOS".to_owned(),
        os_version,
        architecture: "aarch64".to_owned(),
        cpu_model: cpu_model.clone(),
        logical_cores,
        total_physical_memory_bytes,
        gpu_adapter_name: cpu_model,
        gpu_vendor: 0,
        gpu_device: 0,
        gpu_device_class: "integrated".to_owned(),
        wgpu_backend: "metal".to_owned(),
        driver: None,
        driver_metadata_available: false,
        memory_architecture: "unified".to_owned(),
        acceptance_label: "m4-mac-mini-32gb".to_owned(),
    })
}

fn load_inputs(repository_root: &Path) -> Result<(Vec<u8>, Vec<u8>, RegionConfig), CommandError> {
    let config_path = repository_root.join(REGION_CONFIG_PATH);
    let config_bytes = read(&config_path)?;
    let config = ron::de::from_bytes(&config_bytes).map_err(|error| CommandError::Parse {
        path: config_path.display().to_string(),
        error: error.to_string(),
    })?;
    let stamp_path = repository_root.join(RUIN_STAMP_PATH);
    let stamp_bytes = read(&stamp_path)?;
    Ok((config_bytes, stamp_bytes, config))
}

fn read(path: &Path) -> Result<Vec<u8>, CommandError> {
    fs::read(path).map_err(|error| CommandError::Read {
        path: path.display().to_string(),
        error: error.to_string(),
    })
}

fn canonical_manifest_ron(manifest: &CuratedManifest) -> Result<String, CommandError> {
    let body = ron::ser::to_string_pretty(
        manifest,
        ron::ser::PrettyConfig::new()
            .depth_limit(4)
            .separate_tuple_members(true)
            .enumerate_arrays(true),
    )
    .map_err(|error| CommandError::Parse {
        path: CURATED_MANIFEST_PATH.to_owned(),
        error: error.to_string(),
    })?;
    Ok(format!("{GENERATED_FILE_COMMENT}{body}\n"))
}

fn check_manifest(
    manifest_path: &Path,
    canonical: &str,
    config: &RegionConfig,
) -> Result<(), CommandError> {
    let source = fs::read_to_string(manifest_path).map_err(|error| CommandError::Read {
        path: manifest_path.display().to_string(),
        error: error.to_string(),
    })?;
    if source != canonical {
        return Err(CommandError::NonCanonical {
            path: manifest_path.display().to_string(),
        });
    }
    let manifest: CuratedManifest =
        ron::de::from_str(&source).map_err(|error| CommandError::Parse {
            path: manifest_path.display().to_string(),
            error: error.to_string(),
        })?;
    validate_manifest(config, &manifest).map_err(CommandError::Curation)?;
    Ok(())
}

fn write(path: &Path, contents: String) -> Result<(), CommandError> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|error| CommandError::Write {
            path: parent.display().to_string(),
            error: error.to_string(),
        })?;
    }
    fs::write(path, contents).map_err(|error| CommandError::Write {
        path: path.display().to_string(),
        error: error.to_string(),
    })
}

#[cfg(test)]
mod tests {
    use std::{fs, path::Path};

    use moria_world::telemetry::ForestFeasibilityReport;

    use super::{CommandError, run};

    #[test]
    fn check_validates_the_checked_in_manifest() {
        let repository_root = Path::new(env!("CARGO_MANIFEST_DIR")).join("../..");
        assert!(run(["check".to_owned()], &repository_root).is_ok());
    }

    #[test]
    fn rejects_unknown_commands() {
        assert!(matches!(
            run(["unknown".to_owned()], Path::new(".")),
            Err(CommandError::Usage)
        ));
    }

    #[test]
    fn prove_forest_writes_a_validated_feasibility_artifact() {
        let repository_root = Path::new(env!("CARGO_MANIFEST_DIR")).join("../..");
        let output = std::env::temp_dir().join(format!(
            "moria-curate-forest-proof-{}.json",
            std::process::id()
        ));
        let relative_output = output.to_str().unwrap().to_owned();

        run(
            [
                "prove-forest".to_owned(),
                "--output".to_owned(),
                relative_output,
            ],
            &repository_root,
        )
        .unwrap();

        let proof: ForestFeasibilityReport =
            serde_json::from_slice(&fs::read(&output).unwrap()).unwrap();
        proof.validate().unwrap();
        fs::remove_file(output).unwrap();
    }
}
