#[cfg(not(test))]
fn display_profile_fields(
    document: &str,
) -> Result<(String, u32, u32, String, String, String), CommandError> {
    let displays: serde_json::Value = serde_json::from_str(document)
        .map_err(|_| CommandError::Report(ReportValidationError::Identity { field: "gpu" }))?;
    let display = displays
        .get("SPDisplaysDataType")
        .and_then(serde_json::Value::as_array)
        .and_then(|entries| entries.first())
        .ok_or(CommandError::Report(ReportValidationError::Identity {
            field: "gpu",
        }))?;
    let adapter = display
        .get("sppci_model")
        .and_then(serde_json::Value::as_str)
        .filter(|value| !value.is_empty())
        .ok_or(CommandError::Report(ReportValidationError::Identity {
            field: "gpu",
        }))?
        .to_owned();
    let vendor = display
        .get("spdisplays_vendor")
        .and_then(serde_json::Value::as_str)
        .and_then(hex_identifier)
        .ok_or(CommandError::Report(ReportValidationError::Identity {
            field: "gpu",
        }))?;
    let device = display
        .get("spdisplays_device-id")
        .and_then(serde_json::Value::as_str)
        .and_then(hex_identifier)
        .ok_or(CommandError::Report(ReportValidationError::Identity {
            field: "gpu",
        }))?;
    let backend = display
        .get("spdisplays_metal")
        .and_then(serde_json::Value::as_str)
        .filter(|value| value.contains("Metal"))
        .map(|_| "metal".to_owned())
        .ok_or(CommandError::Report(ReportValidationError::Identity {
            field: "gpu",
        }))?;
    Ok((
        adapter,
        vendor,
        device,
        "integrated".to_owned(),
        backend,
        "unified".to_owned(),
    ))
}

#[cfg(not(test))]
fn hex_identifier(value: &str) -> Option<u32> {
    let digits = value
        .split_whitespace()
        .find_map(|piece| piece.strip_prefix("0x"))?;
    u32::from_str_radix(digits, 16).ok()
}

fn load_inputs(repository_root: &Path) -> Result<(RegionConfig, SparseVoxelStamp), CommandError> {
    let config_path = repository_root.join(REGION_CONFIG_PATH);
    let config_bytes = read(&config_path)?;
    let config = ron::de::from_bytes(&config_bytes).map_err(|error| CommandError::Parse {
        path: config_path.display().to_string(),
        error: error.to_string(),
    })?;
    let stamp_path = repository_root.join(RUIN_STAMP_PATH);
    let stamp_bytes = read(&stamp_path)?;
    let stamp = ron::de::from_bytes(&stamp_bytes).map_err(|error| CommandError::Parse {
        path: stamp_path.display().to_string(),
        error: error.to_string(),
    })?;
    Ok((config, stamp))
}

fn read(path: &Path) -> Result<Vec<u8>, CommandError> {
    fs::read(path).map_err(|error| CommandError::Read {
        path: path.display().to_string(),
        error: error.to_string(),
    })
}

fn canonical_manifest_ron(manifest: &CuratedManifest) -> Result<String, CommandError> {
    let body = ron::ser::to_string_pretty(
        manifest,
        ron::ser::PrettyConfig::new()
            .depth_limit(4)
            .separate_tuple_members(true)
            .enumerate_arrays(true),
    )
    .map_err(|error| CommandError::Parse {
        path: CURATED_MANIFEST_PATH.to_owned(),
        error: error.to_string(),
    })?;
    Ok(format!("{GENERATED_FILE_COMMENT}{body}\n"))
}

fn check_manifest(
    manifest_path: &Path,
    canonical: &str,
    config: &RegionConfig,
    stamp: &SparseVoxelStamp,
) -> Result<(), CommandError> {
    let source = fs::read_to_string(manifest_path).map_err(|error| CommandError::Read {
        path: manifest_path.display().to_string(),
        error: error.to_string(),
    })?;
    if source != canonical {
        return Err(CommandError::NonCanonical {
            path: manifest_path.display().to_string(),
        });
    }
    let manifest: CuratedManifest =
        ron::de::from_str(&source).map_err(|error| CommandError::Parse {
            path: manifest_path.display().to_string(),
            error: error.to_string(),
        })?;
    validate_manifest_with_stamp(config, &manifest, stamp).map_err(CommandError::Curation)?;
    Ok(())
}

fn write(path: &Path, contents: String) -> Result<(), CommandError> {
    if let Some(parent) = path.parent() {
        fs::create_dir_all(parent).map_err(|error| CommandError::Write {
            path: parent.display().to_string(),
            error: error.to_string(),
        })?;
    }
    fs::write(path, contents).map_err(|error| CommandError::Write {
        path: path.display().to_string(),
        error: error.to_string(),
    })
}

